package io.calci;


public class Calculator {
	public int add(int num1, int num2)
	{
		return num1+num2;
	}
	public float add(float num1, float num2)
	{
		return num1+num2;
	}
	public int sub(int num1, int num2)
	{
		return num1-num2;
	}
	public float sub(float num1, float num2)
	{
		return num1-num2;
	}


}



